// ── Structured Logger ──────────────────────────────────────────────────────

type LogLevel = "INFO" | "WARN" | "ERROR" | "STEP" | "FIX" | "PASS" | "FAIL";

interface LogEntry {
  ts: string;
  runId: string;
  level: LogLevel;
  step: string;
  message: string;
  durationMs?: number;
  details?: Record<string, unknown>;
}

const logHistory: LogEntry[] = [];

function log(
  level: LogLevel,
  step: string,
  message: string,
  details?: Record<string, unknown>,
  durationMs?: number,
): void {
  const entry: LogEntry = {
    ts: new Date().toISOString(),
    runId: RUN_ID,
    level,
    step,
    message,
    durationMs,
    details,
  };
  logHistory.push(entry);

  const icon =
    level === "PASS"
      ? "✅"
      : level === "FAIL"
        ? "❌"
        : level === "WARN"
          ? "⚠️"
          : level === "FIX"
            ? "🔧"
            : level === "STEP"
              ? "▶️"
              : level === "ERROR"
                ? "💥"
                : "ℹ️";
  const dur = durationMs != null ? ` (${durationMs}ms)` : "";
  console.log(`${icon} [${entry.ts.slice(11, 19)}] [${step}] ${message}${dur}`);
  if (details && Object.keys(details).length > 0) {
    console.log(`   ${JSON.stringify(details)}`);
  }
}

// ── Secret Redaction ───────────────────────────────────────────────────────

const SECRET_PATTERNS = [
  "API_KEY",
  "SECRET",
  "DATABASE_URL",
  "WEBHOOK_URL",
  "JWT_SECRET",
];

function isSecretKey(key: string): boolean {
  const upper = key.toUpperCase();
  return SECRET_PATTERNS.some((p) => upper.includes(p));
}

function redactValue(key: string, value: string): string {
  if (!isSecretKey(key) || !value || value.length < 12) return value;
  return `${value.slice(0, 6)}...${value.slice(-4)}`;
}

// ── .env Parsing & Writing ─────────────────────────────────────────────────

function parseEnvFile(filePath: string): Record<string, string> {
  const vars: Record<string, string> = {};
  if (!fs.existsSync(filePath)) return vars;
  const content = fs.readFileSync(filePath, "utf-8");
  for (const line of content.split("\n")) {
    const trimmed = line.trim();
    if (!trimmed || trimmed.startsWith("#")) continue;
    const eqIdx = trimmed.indexOf("=");
    if (eqIdx === -1) continue;
    const key = trimmed.slice(0, eqIdx).trim();
    let value = trimmed.slice(eqIdx + 1).trim();
    if (
      (value.startsWith('"') && value.endsWith('"')) ||
      (value.startsWith("'") && value.endsWith("'"))
    ) {
      value = value.slice(1, -1);
    }
    vars[key] = value;
  }
  return vars;
}

function writeEnvFile(filePath: string, vars: Record<string, string>): void {
  const lines: string[] = [
    "# ============================================================",
    "# AUTO-GENERATED by factory:smoke — DO NOT EDIT MANUALLY",
    `# Run ID: ${RUN_ID}`,
    `# Generated: ${new Date().toISOString()}`,
    "# Source: .env.example with smoke-test overrides",
    "# ============================================================",
    "",
  ];
  for (const [key, value] of Object.entries(vars)) {
    const needsQuotes =
      value.includes(" ") || value.includes(",") || value.includes("'");
    lines.push(`${key}=${needsQuotes ? `"${value}"` : value}`);
  }
  fs.writeFileSync(filePath, lines.join("\n"), "utf-8");
}

// ── HTTP Helper (native fetch with timeout + cookie + CSRF) ───────────────

let sessionCookie = "";
let csrfToken = "";

async function http<T = any>(
  method: string,
  urlPath: string,
  body?: unknown,
  opts?: { rawResponse?: boolean },
): Promise<{
  status: number;
  ok: boolean;
  data: T;
  headers: Record<string, string>;
  rawBody: string;
}> {
  const controller = new AbortController();
  const timeout = setTimeout(() => controller.abort(), REQUEST_TIMEOUT_MS);

  const headers: Record<string, string> = {
    Accept: "application/json",
  };
  if (sessionCookie) {
    headers["Cookie"] = sessionCookie;
  }
  // Add CSRF token for mutation requests
  if (["POST", "PUT", "DELETE"].includes(method.toUpperCase()) && csrfToken) {
    headers["X-CSRF-Token"] = csrfToken;
  }
  if (body != null) {
    headers["Content-Type"] = "application/json";
  }

  try {
    const response = await fetch(`${BASE_URL}${urlPath}`, {
      method,
      headers,
      body: body != null ? JSON.stringify(body) : undefined,
      signal: controller.signal,
    });

    // Capture Set-Cookie (both admin_session and csrf_token)
    const setCookie = response.headers.get("set-cookie");
    if (setCookie) {
      const adminMatch = setCookie.match(/admin_session=([^;]+)/);
      const csrfMatch = setCookie.match(/csrf_token=([^;]+)/);
      if (adminMatch) {
        sessionCookie = `admin_session=${adminMatch[1]}`;
        // Append csrf_token cookie if present and capture for X-CSRF-Token header
        if (csrfMatch) {
          sessionCookie += `; csrf_token=${csrfMatch[1]}`;
          csrfToken = csrfMatch[1];
        }
      }
    }

    const rawBody = await response.text();
    let data: any;
    try {
      data = JSON.parse(rawBody);
    } catch {
      data = { raw: rawBody.slice(0, 500) };
    }

    const respHeaders: Record<string, string> = {};
    response.headers.forEach((v, k) => {
      respHeaders[k] = v;
    });

    return {
      status: response.status,
      ok: response.ok,
      data,
      headers: respHeaders,
      rawBody,
    };
  } finally {
    clearTimeout(timeout);
  }
}

// ── Server Process Management ──────────────────────────────────────────────

let serverProcess: ChildProcess | null = null;
const serverLogs: string[] = [];

function startServer(): Promise<void> {
  return new Promise((resolve, reject) => {
    const tsNodePath = path.join(SERVER_DIR, "node_modules", ".bin", "ts-node");
    const entryPoint = path.join(SERVER_DIR, "src", "index.ts");

    // Use ts-node directly to match npm run dev behavior
    const isWin = process.platform === "win32";
    const cmd = isWin ? `${tsNodePath}.cmd` : tsNodePath;

    serverProcess = spawn(cmd, [entryPoint], {
      cwd: SERVER_DIR,
      env: {
        ...process.env,
        NODE_ENV: "development",
        // Force .env from our generated path
      },
      stdio: ["ignore", "pipe", "pipe"],
      shell: isWin,
    });

    let booted = false;
    const bootTimeout = setTimeout(() => {
      if (!booted) {
        reject(
          new Error(
            `Server failed to start within ${SERVER_BOOT_TIMEOUT_MS}ms. Last logs:\n${serverLogs.slice(-10).join("\n")}`,
          ),
        );
      }
    }, SERVER_BOOT_TIMEOUT_MS);

    const onData = (chunk: Buffer) => {
      const lines = chunk.toString().split("\n").filter(Boolean);
      for (const line of lines) {
        serverLogs.push(line);
        if (serverLogs.length > 200) serverLogs.shift();
        // Detect server ready
        if (line.includes("Server running on port") && !booted) {
          booted = true;
          clearTimeout(bootTimeout);
          resolve();
        }
      }
    };

    serverProcess.stdout?.on("data", onData);
    serverProcess.stderr?.on("data", onData);

    serverProcess.on("error", (err) => {
      if (!booted) {
        clearTimeout(bootTimeout);
        reject(new Error(`Server process error: ${err.message}`));
      }
    });

    serverProcess.on("exit", (code) => {
      if (!booted) {
        clearTimeout(bootTimeout);
        reject(
          new Error(
            `Server exited with code ${code} before becoming ready. Logs:\n${serverLogs.slice(-15).join("\n")}`,
          ),
        );
      }
    });
  });
}

function stopServer(): void {
  if (serverProcess) {
    serverProcess.kill("SIGTERM");
    serverProcess = null;
  }
}

async function waitForHealth(maxWaitMs = 10_000): Promise<boolean> {
  const start = Date.now();
  while (Date.now() - start < maxWaitMs) {
    try {
      const res = await http("GET", "/health");
      if (res.ok && res.data?.status === "ok") return true;
    } catch {
      // Server not ready yet
    }
    await sleep(500);
  }
  return false;
}

// ── Utilities ──────────────────────────────────────────────────────────────

function sleep(ms: number): Promise<void> {
  return new Promise((r) => setTimeout(r, ms));
}

function timed<T>(fn: () => Promise<T>): Promise<{ result: T; ms: number }> {
  const start = Date.now();
  return fn().then((result) => ({ result, ms: Date.now() - start }));
}

